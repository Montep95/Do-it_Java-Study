package chapter2;

public class CharacterEx {

	public static void main(String[] args) 
	{
		char ch = 'A';
		
		System.out.println(ch); // A가 출력됨
		System.out.println((int)ch); // 65가 출력됨
		
		ch = 66; // 문자에 66을 넣었으므로 B가 출력
		
		int ch2 = 67;
		
		// ch2를 출력하면 67이 출력
		// (char)ch2를 출력하면 C가 출력
		

	}

}
